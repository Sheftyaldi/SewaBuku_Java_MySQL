package com.mycompany.mavenproject1.Model;
import java.sql.Connection;
import java.sql.DriverManager;
import java.sql.SQLException;

public class DatabaseConnection {
    private static final String URL = "jdbc:mysql://localhost:3306/sewa_buku?useSSL=false&serverTimezone=UTC";
    private static final String USER = "root"; // Ganti dengan username MySQL
    private static final String PASSWORD = ""; // Ganti dengan password MySQL
    
    public static Connection getConnection() throws SQLException {
        try {
            Class.forName("com.mysql.cj.jdbc.Driver");
            return DriverManager.getConnection(URL, USER, PASSWORD);
        } catch (ClassNotFoundException e) {
            throw new SQLException("MySQL JDBC Driver not found", e);
        }
    }
}